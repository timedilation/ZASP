// DP with bitmask
#include <iostream>
#include <vector>
using namespace std;

int main(void) {
    int num;
    scanf("%d", &num);
    int costs[num];
    
    for (int i = 0; i < num; i++)
    {
        costs[i] = 0;
    }
    
    for (int i = 0; i < num; i++) {
        for (int j = 0; j < num; j++) {
            int cost;
            scanf("%d", &cost);
        }
    }
    


    return 0;
}